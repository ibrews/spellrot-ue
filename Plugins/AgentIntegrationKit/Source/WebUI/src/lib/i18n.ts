const STORAGE_KEY = 'ui_locale';

function sanitizeLocale(value: string | null | undefined): Locale {
	return locales.includes((value ?? '') as Locale) ? (value as Locale) : 'en';
}

function getInitialLocale(): Locale {
	if (typeof localStorage === 'undefined') return 'en';
	try {
		return sanitizeLocale(localStorage.getItem(STORAGE_KEY));
	} catch {
		return 'en';
	}
}

export const locale = writable<Locale>(getInitialLocale());

locale.subscribe((value) => {
	if (typeof localStorage === 'undefined') return;
	try {
		localStorage.setItem(STORAGE_KEY, value);
	} catch {
		// Ignore storage failures in embedded webviews.
	}
});

export function setLocale(value: Locale): void {
	locale.set(value);
}

export function translate(
	currentLocale: Locale,
	key: string,
	params?: Record<string, string | number>
): string {
	const entry = translations[currentLocale]?.[key] ?? translations.en[key] ?? key;
	return typeof entry === 'function' ? entry(params) : entry;
}

export const t = derived(locale, ($locale) => {
	return (key: string, params?: Record<string, string | number>) => translate($locale, key, params);
});
